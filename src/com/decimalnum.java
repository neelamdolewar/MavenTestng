package com;

public class decimalnum {

    public static void main(String[] args) {
        //ACE hexadecimal number
        int num1 = 0xACE;
        System.out.println("Hexadecimal number  ACE = Decimal number is=" +num1);

       //FFFF hexadecimal number
        int num2 = 0xFFFF;
        System.out.println("Hexadecimal number  FFFF = Decimal number is=" +num2);

        //OCTAL number
        int num3 = 0142;
        System.out.println("Octal number  142 = Decimal number is =" +num3);

        //OCTAL number
        int num4 = 0777;
        System.out.println("Octal number  777 = Decimal number is =" +num4);

        //Binary number
        int num5 = 0B10101010;
        System.out.println("Binary number 10101010 = Decimal number is =" +num5);

        //Binary number
        int num6 = 0B11110000;
        System.out.println("Binary number 11110000 = Decimal number is =" +num6);


    }
}
