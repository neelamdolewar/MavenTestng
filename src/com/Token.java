package com;

public class Token {

    public static void main(String[] args) {

        System.out.println(); //new line

        String s1 = "hello you all are welcome to india";
        System.out.println("String literal example=" + s1);
        System.out.println();//new line

        //suffix with capital D and 12 digits after decimal point
        double d1 = 15.123456789012D;
        System.out.println("Double literal example suffix with capital D =" + d1);
        System.out.println(); // new line

        // suffix with small d and 12 digits after decimal point
        double d2 = 10.256478912345d;
        System.out.println("Double literal  example suffix with small d=" + d2);


    }
}
