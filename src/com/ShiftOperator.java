package com;

public class ShiftOperator {

    public static void main(String[] args) {
        //Left shift 22 by 3
        System.out.println("Left shift 22 by 3 is ="+(22<<3));

        //Right shift 56 by 5
        System.out.println("Right shift 56 by 5 is="+(56>>5));

    }



}
