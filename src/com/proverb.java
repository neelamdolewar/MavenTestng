package com;

public class proverb {

    public static void main(String[] args) {
        //Printing proverb in single quotes:- ( \')
        System.out.println("Today\'s thought is:\'Nothing can be done without hope and confidence\'");

        //Printing proverb in double quotes:- ( \")
        System.out.println("Best proverb : \"Practice makes man perfect \"");


        //Backslash (\b) removing extra spaces in sentences:-
        System.out.println("Blank space example");

        System.out.println("\bsun \b mon \b tue \b wed \b thu \b fri \b sat");





    }
}
