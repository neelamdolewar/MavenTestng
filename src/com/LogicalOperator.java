package com;

public class LogicalOperator {

    public static void main(String[] args) {

        // AND operator

        int a = 2;
        int b = 3;

        System.out.println("Result of AND operator is =" + (a & b));

        // OR operator
        int c = 10;
        int d = 20;
        System.out.println("Result of OR operator is =" + (c | d));
    }
}

